class Season{
public static void main(String args[]){
String Month="Winter";
switch(Month){
case "Winter":System.out.println("January ,December, February");
break;
case "Spring":System.out.println("March , April, May");
break;
case "Autumn":System.out.println("September, October, November");
break;
case "Summer":System.out.println("June, July, August");
break;
default:System.out.println("Invalid season");
}}}